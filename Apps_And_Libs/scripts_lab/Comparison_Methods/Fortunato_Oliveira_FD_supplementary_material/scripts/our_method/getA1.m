function A1 = getA1(R,C)
% A1:
a1 = zeros(R, C);

% use_dx
    a1(1  , 1) = 1;
    a1(1  , 3) = -0.25; a1(1  ,C-1) = -0.25;
    a1(R-1, 1) = -0.25; a1(3  ,1  ) = -0.25;

% use_dxx
    a1(1,  1) = a1(1,  1) + 6;
    a1(1,  2) = a1(1,  2) - 2;
    a1(1,  C) = a1(1,  C) - 2;
    a1(R,  1) = a1(R,  1) - 2;
    a1(2,  1) = a1(2,  1) - 2;
    a1(1,  3) = a1(1,  3) + 0.5;
    a1(1,C-1) = a1(1,C-1) + 0.5;
    a1(R-1,1) = a1(R-1,1) + 0.5;
    a1(3,  1) = a1(3,  1) + 0.5;

% use_dxy
    a1(1, 1) = a1(1, 1) + 8;
    a1(1, 2) = a1(1, 2) - 4;
    a1(1, C) = a1(1, C) - 4;
    a1(R, 1) = a1(R, 1) - 4;
    a1(2, 1) = a1(2, 1) - 4;
    a1(2, 2) = a1(2, 2) + 2;
    a1(R, C) = a1(R, C) + 2;
    a1(R, 2) = a1(R, 2) + 2;
    a1(2, C) = a1(2, C) + 2;


 A1 = real(fft2(a1));
